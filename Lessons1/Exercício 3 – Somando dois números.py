# Crie um programa que leia dois números e mostre a soma entre eles.

numero1 = int(input('Digite o primeiro número: '))
numero2 = int(input('Digite o segundo número: '))

# A soma, assm como normalmente, é efetuada com o sinal de (+), podendo ser feita dentro do (print()) com o (.format)

print('A soma entre os números: {} e {}, é: {}'.format(numero1, numero2, numero1 + numero2))
